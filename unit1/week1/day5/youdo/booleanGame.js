// Game of Booleans
// Part 1 (Output : False)
console.log( true || false || false && false,"not possible" );

// Part 2 (Output : True)
console.log( true || true && false || true );

// Part 3 (Output : False)
console.log( false || true || false || false && true,"not possible" );

// Part 4 (Output : True)
console.log( false && true || false || false && false,"not possible" );

// Part 5 (Output : False)
console.log(false || false || false && true || false);