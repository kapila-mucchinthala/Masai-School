var grade = "A";
 switch (grade) {
    case "A":
        console.log("Excellent");
        break;
    case "B":
        console.log("Very Good");
        break;
    case "C":
        console.log("Good");
        break;
    case "D":
        console.log("Needs a lot of improvement");
        break;
    case "F":
        console.log("Failed");
        break;
    default:
        console.log("Please fill in correct grade");
}