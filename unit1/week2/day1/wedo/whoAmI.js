var numOfSides = 3;

if (numOfSides == 3) {
    console.log("Triangle");
} else if (numOfSides == 4) {
    console.log("Quadrilateral");
} else if (numOfSides == 5) {
    console.log("Pentagon");
} else if (numOfSides == 6) {
    console.log("Hexagon");
} else {
    console.log("Please refer google");
}